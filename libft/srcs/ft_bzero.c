#include "libft.h"

void	ft_bzero(void *s, size_t l)
{
	char	c;
	char	*str;

	c = '\0';
	str = (char *)s;
	while (l)
	{
		(*str = c);
		*str = '\0';
		str++;
		l--;
	}
}
